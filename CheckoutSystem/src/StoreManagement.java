    private void buttonAccAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAccAddActionPerformed
        try{
            if(txtAccName.getText().isEmpty() || txtAccPhoneNo.getText().isEmpty() || txtAccPin.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
            }
            else{
                if(txtAccRole.getSelectedItem().toString().equals("Cashier")){
                    DatabaseFunctions.addAccount(Integer.parseInt(txtAccPin.getText()), txtAccName.getText(), true, false, Long.parseLong(txtAccPhoneNo.getText()));
                }
                else if((txtAccRole.getSelectedItem().toString().equals("Manager"))){
                    DatabaseFunctions.addAccount(Integer.parseInt(txtAccPin.getText()), txtAccName.getText(), false, true, Long.parseLong(txtAccPhoneNo.getText()));
                }
                else{
                    DatabaseFunctions.addAccount(Integer.parseInt(txtAccPin.getText()), txtAccName.getText(), true, true, Long.parseLong(txtAccPhoneNo.getText()));
                }
                
                JOptionPane.showMessageDialog(this, "Account Successfully Added");
                DisplayTable();
                txtAccName.setText("");
                txtAccPhoneNo.setText("");
                txtAccPin.setText("");
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(this, "Input Correct Information");
        }
    }//GEN-LAST:event_buttonAccAddActionPerformed

    private void buttonAccDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAccDeleteActionPerformed
        try{
            c = connect();
            DefaultTableModel model = (DefaultTableModel)tableAccount.getModel();
            int tbIndex = tableAccount.getSelectedRow();
            String Id = model.getValueAt(tbIndex, 0).toString();
            String Query = "Delete from Account where account_id="+Id;
            Statement ADD = c.createStatement();
            ADD.executeUpdate(Query);
            JOptionPane.showMessageDialog(this, "Account Successfully Deleted");
            DisplayTable();
            txtAccName.setText("");
            txtAccPhoneNo.setText("");
            txtAccPin.setText("");
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Select the row from table to Delete");
        }
    }//GEN-LAST:event_buttonAccDeleteActionPerformed

