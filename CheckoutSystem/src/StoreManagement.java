    public void DisplayTable(){
        try{
            Class.forName("org.sqlite.JDBC");
            c = connect();
            String sqlAcc = "select * from Account";
            PreparedStatement pstmtAcc = c.prepareStatement(sqlAcc);
            ResultSet rsAcc = pstmtAcc.executeQuery();
            tableAccount.setModel(DbUtils.resultSetToTableModel(rsAcc));
            
            String sqlInv = "select * from Inventory";
            PreparedStatement pstmtInv = c.prepareStatement(sqlInv);
            ResultSet rsInv = pstmtInv.executeQuery();
            tableInventory.setModel(DbUtils.resultSetToTableModel(rsInv));
            
            String sqlOrd = "select * from Orders";
            PreparedStatement pstmtOrd = c.prepareStatement(sqlOrd);
            ResultSet rsOrd = pstmtOrd.executeQuery();
            tableOrders.setModel(DbUtils.resultSetToTableModel(rsOrd));
            
            String sqlPur = "select * from Purchase";
            PreparedStatement pstmtPur = c.prepareStatement(sqlPur);
            ResultSet rsPur = pstmtPur.executeQuery();
            tablePurchase.setModel(DbUtils.resultSetToTableModel(rsPur));
        }
        catch(Exception e){
            
        }
    }
