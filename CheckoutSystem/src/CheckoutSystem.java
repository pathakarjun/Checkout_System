    public String itemAmount(){
        String amt = jTable.getValueAt(0,2).toString();
        for (int i = 1; i< jTable.getRowCount();i++){
            amt = amt + ", " + jTable.getValueAt(i,2).toString();
    }
        return amt;
    }
    
    public String itemId(){
        String id = jTable.getValueAt(0,0).toString();
        for (int i = 1; i< jTable.getRowCount();i++){
            id = id + ", " + jTable.getValueAt(i,0).toString();
    }
        return id;
    }   
    
 
 
    
