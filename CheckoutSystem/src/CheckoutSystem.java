    public  double itemCost(){
        double sum = 0;
        for (int i = 0; i <jTable.getRowCount();i++){
            sum = sum + Double.parseDouble(jTable.getValueAt(i, 3).toString());
        }
        txtSubtotal.setText(Double.toString(sum));
        double cSubtotal = Double.parseDouble(txtSubtotal.getText());
        
        double cTax = (cSubtotal * 8)/100;
        String displayTax = String.format("$ %.2f", cTax);
        txtTax.setText(displayTax);
        
        String displaySubtotal = String.format("$ %.2f", cSubtotal);
        txtSubtotal.setText(displaySubtotal);
        
        double cTotal = cSubtotal + cTax;
        String displayTotal = String.format("$ %.2f", cTotal);
        txtTotal.setText(displayTotal);
        
        return(cTotal);
    }
    
        public void receipt(){
        String itotal = txtTotal.getText();
        
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        receipt.setText("");
        receipt.setText(receipt.getText() + "*********************************************************\n");
        receipt.setText(receipt.getText() + "*                       Checkout System                        *\n");
        receipt.setText(receipt.getText() + "*********************************************************\n");
        receipt.setText(receipt.getText() + "Product"+"\t"+"QTY"+"\t"+"Amount"+"\n");
        
        for(int i = 0; i< model.getRowCount();i++){
            String product = model.getValueAt(i,1).toString();
            String QTY = model.getValueAt(i,2).toString();
            String amount = model.getValueAt(i,3).toString();
            receipt.setText(receipt.getText() + product +"\t"+ QTY +"\t"+ amount +"\n");
        }
        receipt.setText(receipt.getText() + "\n");
        receipt.setText(receipt.getText() + "\t"+"\t"+"Total :"+"\t"+ itotal + "\n");
    }
    
    
