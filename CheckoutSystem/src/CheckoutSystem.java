    private void checkPayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkPayButtonActionPerformed
        String str = txtCheckTotal.getText();
        str = str.replaceAll("[^\\.0123456789]","");
        double total = Double.parseDouble(str);
        Random rand = new Random(); //instance of random class
        //generate random values from 0-24
        int int_random = rand.nextInt(2147483646);
        String itemAmt = itemAmount();
        String itemList = itemId();
        boolean validity = CheckoutControl.check(total, Long.parseLong(txtCheckNumber.getText()), itemAmt,itemList,int_random);
        if (validity == true){
            txtCheckPaymentConfirmed.setText("Valid Check");
            String strTotal = txtCheckTotal.getText();
            strTotal = strTotal.replaceAll("[^\\.0123456789]","");
            double totalAmt = Double.parseDouble(strTotal);
            double checkAmt = Double.parseDouble(txtAmount.getText());
            double checkChange = (checkAmt - totalAmt);
        
            String checkChangeAmt = String.format("$ %.2f", checkChange);
            txtCheckChange.setText(checkChangeAmt);
            try{
                DatabaseFunctions.updateCredit(Long.parseLong(phoneNo.getText()), total);
            }
            catch(Exception e){
                
            }
            jPanel5.setVisible(true);
            jPanel4.setVisible(true);
        }
        else{
            txtCheckPaymentConfirmed.setText("Invalid Check.");
        } 
    }//GEN-LAST:event_checkPayButtonActionPerformed

