    private void cashPayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashPayButtonActionPerformed
        String str = txtCashTotal.getText();
        str = str.replaceAll("[^\\.0123456789]","");
        double total = Double.parseDouble(str);
        double cash = Double.parseDouble(txtCash.getText());
        double cChange = (cash - total);

        if(cash >= total){
            Random rand = new Random(); //instance of random class
            //generate random values from 0-24
            int int_random = rand.nextInt(2147483646);
            String itemAmt = itemAmount();
//            System.out.println(itemAmt);
            String itemList = itemId();
//            System.out.println(itemList);
            CheckoutControl.cash(total,cash,itemAmt,itemList,int_random);
            String ChangeAmt = String.format("$ %.2f", cChange);
            txtChange.setText(ChangeAmt);
            try{
                DatabaseFunctions.updateCredit(Long.parseLong(phoneNo.getText()), total);
            }
            catch(Exception e){
                
            }
            jPanel.setVisible(true);
            txtTillStatus.setText("OPEN");
        }
        else{
            JOptionPane.showMessageDialog(this, "Not enough Cash");
            txtCash.setText("");
            txtChange.setText("");
        }
        
    }//GEN-LAST:event_cashPayButtonActionPerformed

